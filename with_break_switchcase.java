package Capgemini;

public class with_break_switchcase {
	public static void main(String[] args) {
		int choice = 2;
		switch(choice) {
		case 1: {
			System.out.println("C1");
		}
		break;
		case 2: {
			System.out.println("C2");
		}
		break;
		case 3: {
			System.out.println("C3");
		}
		break;
		default: {
			System.out.println("No match");
		}
		}
	}
}
