package Capgemini;

public class Widening {
	public static void main(String[] args) {
		int a = 10;
		long b = a;
		System.out.println(b);
		
		byte g = 3;
		float h = (float)g;
		System.out.println(h);
		
		char me = 'a';
		int p3 = (int)me;
		System.out.println(p3);
		
		float j = 99.9f;
		double p5 = j;
		System.out.println(p5);
	}
}
