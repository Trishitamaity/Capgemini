package Non_Primitive_Typecasting;

public class Engine {
	private int cc;
	private String type;
	public int getCc() {
		return cc;
	}
	public String getType() {
		return type;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}
	public void setType(String type) {
		this.type = type;
	}
}